- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath {
    //[super tableView:tableView didSelectRowAtIndexPath:indexPath];
    

    
    PFObject *category;
    if(indexPath.row == 0){
        category = [PFObject objectWithoutDataWithClassName:@"Category" objectId:@"ORqAwEdAtK"];
    }
    
    if(indexPath.row == 1){
        category = [PFObject objectWithoutDataWithClassName:@"Category" objectId:@"re5rF1jMkb"];
    }
    
    if(indexPath.row == 2){
        category = [PFObject objectWithoutDataWithClassName:@"Category" objectId:@"O30KnNmp2l"];
    }
    
    if(indexPath.row == 3){
        category = [PFObject objectWithoutDataWithClassName:@"Category" objectId:@"B5HNeL8Clg"];
    }
    
    
    NSDictionary *userInfo = [NSDictionary dictionaryWithObject:category forKey:@"object"];
    
    [[NSNotificationCenter defaultCenter] postNotificationName: @"close" object:nil userInfo:userInfo];
}

